public class ConcurrentNavigableMapTest {
    public static void main(String[] args) {
        int i = 0;
        if((i = 7 - 5) == 2)
            System.out.println(i);
            System.out.println(i - 1);
            System.out.println(i - 1);
            System.out.println(i - 1);
            System.out.println(i - 1);
            System.out.println(i - 1);
    }
}
